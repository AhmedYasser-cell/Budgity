package src.FinanceCore;

/**
 * Enum representing different categories for transactions.
 */
public enum Category {
    /** Essential food and household supplies. */
    GROCERIES,
    /** Monthly rental or mortgage payments. */
    RENT,
    /** Essential services like electricity, water, and gas. */
    UTILITIES,
    /** Income from employment. */
    SALARY,
    /** Leisure and recreational activities. */
    ENTERTAINMENT,
    /** Any other miscellaneous category. */
    OTHER
}